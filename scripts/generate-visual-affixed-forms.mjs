import { writeFile } from "node:fs/promises";
import { resolve } from "node:path";

import { wordToIthkuil } from "@zsnout/ithkuil/generate/index.js";
import { parseWord } from "@zsnout/ithkuil/parse/index.js";

import {
  DEFAULT_VISUAL_AFFIX_DEGREES,
  IKAL_VISUAL_AFFIXES,
  IKAL_VISUAL_SOURCE_ROOTS,
} from "../src/parser/ikal-visual-affixes.js";
import { IKAL_SEED_ROOTS } from "../src/parser/ithkuil-seed-roots.js";

const OUTPUT_PATH = resolve("src/parser/generated/ikal-visual-affixed-forms.js");

function plainAffix(definition, degree) {
  return {
    cs: definition.cs,
    degree,
    type: definition.type,
  };
}

function combinations(items, size, start = 0, prefix = []) {
  if (prefix.length === size) {
    return [prefix];
  }

  const result = [];

  for (let i = start; i < items.length; i++) {
    result.push(...combinations(items, size, i + 1, [...prefix, items[i]]));
  }

  return result;
}

function generatedWord(seed, slotVIIAffixes, slotVAffixes = []) {
  return wordToIthkuil({
    ca: seed.ca || {},
    function: seed.function || "STA",
    root: seed.cr,
    shortcut: false,
    slotVAffixes,
    slotVIIAffixes,
    stem: seed.stem || 1,
    type: "UNF/C",
  });
}

function normalizeAffixes(affixes) {
  return affixes.map((affix) => ({
    cs: affix.cs,
    degree: affix.degree,
    type: affix.type,
  }));
}

function verifyGeneratedForm(entry) {
  const parsed = parseWord(entry.form);

  if (parsed.root !== entry.root) {
    throw new Error("generated form root mismatch for " + entry.form);
  }

  if ((parsed.stem || 1) !== (entry.stem || 1)) {
    throw new Error("generated form stem mismatch for " + entry.form);
  }

  const expectedSlotV = entry.slotVAffixes || [];
  const expectedSlotVII = entry.slotVIIAffixes || [];

  if (JSON.stringify(normalizeAffixes(parsed.slotVAffixes || [])) !== JSON.stringify(normalizeAffixes(expectedSlotV))) {
    throw new Error("generated form Slot V mismatch for " + entry.form);
  }

  if (JSON.stringify(normalizeAffixes(parsed.slotVIIAffixes || [])) !== JSON.stringify(normalizeAffixes(expectedSlotVII))) {
    throw new Error("generated form Slot VII mismatch for " + entry.form);
  }
}

export function generateVisualAffixedForms() {
  const visualSeeds = IKAL_SEED_ROOTS.filter((seed) => IKAL_VISUAL_SOURCE_ROOTS.includes(seed.cr));
  const entries = [];
  const seen = new Set();

  function addEntry(seed, kind, slotVIIAffixes, slotVAffixes = []) {
    const form = generatedWord(seed, slotVIIAffixes, slotVAffixes);
    const key = seed.form + "::" + form;

    if (seen.has(key)) {
      return;
    }

    const entry = {
      baseForm: seed.form,
      form,
      kind,
      root: seed.cr,
      stem: seed.stem || 1,
    };

    if (slotVAffixes.length > 0) {
      entry.slotVAffixes = slotVAffixes;
    }

    if (slotVIIAffixes.length > 0) {
      entry.slotVIIAffixes = slotVIIAffixes;
    }

    verifyGeneratedForm(entry);
    seen.add(key);
    entries.push(entry);
  }

  for (const seed of visualSeeds) {
    for (const definition of IKAL_VISUAL_AFFIXES) {
      for (let degree = 1; degree <= 9; degree++) {
        addEntry(seed, "single", [plainAffix(definition, degree)]);
      }
    }

    for (const combo of combinations(IKAL_VISUAL_AFFIXES, 2)) {
      addEntry(seed, "combo-default", combo.map((definition) => (
        plainAffix(definition, DEFAULT_VISUAL_AFFIX_DEGREES[definition.abbreviation])
      )));
    }

    addEntry(
      seed,
      "diagnostic-slot-v",
      [],
      [plainAffix(IKAL_VISUAL_AFFIXES[0], DEFAULT_VISUAL_AFFIX_DEGREES[IKAL_VISUAL_AFFIXES[0].abbreviation])],
    );
  }

  return entries;
}

export function formatGeneratedVisualAffixedForms(entries = generateVisualAffixedForms()) {
  return [
    "// Generated by scripts/generate-visual-affixed-forms.mjs.",
    "// Do not edit by hand.",
    "export const IKAL_GENERATED_VISUAL_AFFIX_FORMS = " + JSON.stringify(entries, null, 2) + ";",
    "",
  ].join("\n");
}

if (import.meta.url === new URL(process.argv[1], "file:").href) {
  await writeFile(OUTPUT_PATH, formatGeneratedVisualAffixedForms());
  console.log("generated " + OUTPUT_PATH);
}
