import { writeFile } from "node:fs/promises";
import { resolve } from "node:path";

import { wordToIthkuil } from "@zsnout/ithkuil/generate/index.js";
import { parseWord } from "@zsnout/ithkuil/parse/index.js";

import { IKAL_MARKED_AFFILIATIONS } from "../src/parser/ikal-affiliations.js";
import { IKAL_SEED_ROOTS } from "../src/parser/ithkuil-seed-roots.js";

const OUTPUT_PATH = resolve("src/parser/generated/ikal-affiliation-forms.js");

// Les mots de matière du moteur visuel : primitives d'image et mots de
// mouvement (vocabulaire symétrique entre lyala: et lyula:). Les mots glitch
// (affrala, sčala) n'y sont pas : ils transforment toute la ligne, l'opérateur
// de conjugaison ne s'applique pas à eux.
const MATTER_ROOTS = ["llw", "šp", "fř", "ft", "vt", "fth", "mzm", "tçv", "tr", "gl"];

function generatedWord(seed, affiliation) {
  return wordToIthkuil({
    ca: { ...(seed.ca || {}), affiliation },
    function: seed.function || "STA",
    root: seed.cr,
    shortcut: false,
    slotVAffixes: [],
    slotVIIAffixes: [],
    stem: seed.stem || 1,
    type: "UNF/C",
  });
}

function verifyGeneratedForm(entry, seed) {
  const parsed = parseWord(entry.form);

  if (parsed.root !== entry.root) {
    throw new Error("generated form root mismatch for " + entry.form);
  }

  if ((parsed.stem || 1) !== (entry.stem || 1)) {
    throw new Error("generated form stem mismatch for " + entry.form);
  }

  if ((parsed.function || "STA") !== (seed.function || "STA")) {
    throw new Error("generated form function mismatch for " + entry.form);
  }

  if (parsed.ca?.affiliation !== entry.affiliation) {
    throw new Error("generated form affiliation mismatch for " + entry.form);
  }

  for (const [key, value] of Object.entries(seed.ca || {})) {
    if (parsed.ca?.[key] !== value) {
      throw new Error("generated form Ca mismatch (" + key + ") for " + entry.form);
    }
  }
}

export function generateAffiliationForms() {
  const entries = [];
  const seen = new Set();

  for (const cr of MATTER_ROOTS) {
    const seed = IKAL_SEED_ROOTS.find((root) => root.cr === cr);

    if (!seed) {
      throw new Error("racine de matière visuelle inconnue : " + cr);
    }

    for (const { code } of IKAL_MARKED_AFFILIATIONS) {
      const form = generatedWord(seed, code);

      if (form === seed.form || seen.has(form)) {
        throw new Error("generated affiliation form collision for " + form);
      }

      const entry = {
        affiliation: code,
        baseForm: seed.form,
        form,
        kind: "affiliation",
        root: seed.cr,
        stem: seed.stem || 1,
      };

      verifyGeneratedForm(entry, seed);
      seen.add(form);
      entries.push(entry);
    }
  }

  return entries;
}

export function formatGeneratedAffiliationForms(entries = generateAffiliationForms()) {
  return [
    "// Generated by scripts/generate-affiliation-forms.mjs.",
    "// Do not edit by hand.",
    "export const IKAL_GENERATED_AFFILIATION_FORMS = " + JSON.stringify(entries, null, 2) + ";",
    "",
  ].join("\n");
}

if (import.meta.url === new URL(process.argv[1], "file:").href) {
  await writeFile(OUTPUT_PATH, formatGeneratedAffiliationForms());
  console.log("generated " + OUTPUT_PATH);
}
